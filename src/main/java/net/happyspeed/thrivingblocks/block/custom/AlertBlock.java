public class AlertBlock extends BlockWithEntity implements BlockEntityProvider {

    public AlertBlock(Settings settings) {
        super(settings);
    }

    @Override
    public BlockEntity createBlockEntity(BlockPos pos, BlockState state) {
        return new AlertBlockEntity(pos, state);
    }
    @Override
    public BlockRenderType getRenderType(BlockState state) {
        // With inheriting from BlockWithEntity this defaults to INVISIBLE, so we need to change that!
        return BlockRenderType.MODEL;
    }

    @Override
    public <T extends BlockEntity> BlockEntityTicker<T> getTicker(World world, BlockState state, BlockEntityType<T> type) {
        return checkType(type, ModBlocks.ALERT_BLOCK_ENTITY_BLOCK_ENTITY_TYPE, (world1, pos, state1, be) -> AlertBlockEntity.tick(world1, pos, state1, be));
    }
}

